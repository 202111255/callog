package org.techtown.callog;

public class UserAccount {

    private String idToken;
    private String emailId;
    private String password;
    private String name;
    private String signbirth1;
    private String signbirth2;
    private String signbirth3;

    public UserAccount() { }
    public  String getIdToken() {return idToken;}
    public void setIdToken(String idToken) {this.idToken = idToken;}

    public  String getEmailId() {return emailId;}
    public void setEmailId(String emailId) {this.emailId = emailId;}

    public  String getPassword() {return password;}
    public void setPassword(String password) {this.password = password;}

    public  String getName() {return name;}
    public void setName(String name) {this.name = name;}

    public  String getSignbirth1() {return signbirth1;}
    public void setSignbirth1(String signbirth1) {this.signbirth1 = signbirth1;}
    public  String getSignbirth2() {return signbirth2;}
    public void setSignbirth2(String signbirth2) {this.signbirth2 = signbirth2;}
    public  String getSignbirth3() {return signbirth3;}
    public void setSignbirth3(String signbirth3) {this.signbirth3 = signbirth3;}

}
